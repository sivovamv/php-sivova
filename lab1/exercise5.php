<?php
$f = 'string'; // заданная переменная
$length = strlen($f); // длина строки
$sum = 0; // переменная для суммы
$count = 0; // счетчик натуральных чисел

// цикл для подсчета суммы первых $length натуральных чисел
for ($i = 1; $i <= $length; $i++) {
    $sum += $i; // добавляем текущий номер к сумме
    $count++; // увеличиваем счетчик
}

// вывод результата
echo "Длина строки: $length<br>";
echo "Сумма первых $length натуральных чисел: $sum<br>";
echo "Количество чисел, посчитанных в цикле: $count";
?>
