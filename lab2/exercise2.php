<?php
// Два массива
$array1 = [1, 2, 3];
$array2 = ['a', 'b', 'c'];

// Объединение массивов
$result = array_merge($array1, $array2);

// Вывод результата
print_r($result);
?>
<!-- array_merge. Даны два массива: первый с элементами 1, 2, 3, второй с элементами 'a', 'b', 'c'. Сделайте из них массив с элементами 1, 2, 3, 'a', 'b', 'c'. -->